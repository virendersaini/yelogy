    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $package = Package::create($request->all());
        if($package){
            if(!empty($request->hasFile('packageimage'))){                
                 $package->addMedia($request->file('packageimage'))->toMediaCollection('packageimage');
            }
        }

        if($package){
            return redirect(route('packages.index'))->with('success','Package added successfully.');
        }else{
            return back()->with('error','Unable to create package');
        }
    }

